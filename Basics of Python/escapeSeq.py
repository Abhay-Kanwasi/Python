# escape sequence or charcaters in python

print("I am \nabhay bhardwaj")  # skips to next line

print("1.hello\t2.my\t3.name\t4.is\t5.abhay")  # gives spacing in string

print('It\'s, python language')  # \ is used to escape charcter in programming

print("""This is a "great" course,""")

# raw string

# r : used to declear raw string
print(r"https:\\www.google.com\temp\new_course.cpp")

# \b shift string one step back
print("abhay\bbhardwaj")

# \r : carriage return

print("abhay bhardwaj \rhello")
